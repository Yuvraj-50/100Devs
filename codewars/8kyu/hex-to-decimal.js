// Complete the function which converts hex number (given as a string) to a decimal number.

// solution ''''''''''''''''''''''''''''-'-----------''-'-'-'-'-'-'-'-'-'-'-''-

function hexToDec(hexString) {
  return parseInt(hexString, 16);
}
